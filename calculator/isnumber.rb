def isNum(strinToChek)
    return strinToChek == "0" || strinToChek == "1" || strinToChek == "2" || strinToChek == "3" || strinToChek == "4" || strinToChek == "5" || strinToChek == "6" || strinToChek == "7" || strinToChek == "8" || strinToChek == "9"
end

def isOperator(steinToChek)
    return steinToChek == "+" || steinToChek == "-" || steinToChek == "*" || steinToChek == "/"
end